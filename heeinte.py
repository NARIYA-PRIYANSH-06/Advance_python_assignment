'''BaseException. Here are some commonly used built-in exception classes:

Exception: Almost all built-in, non-system-exiting exceptions are derived from this class. It's a good practice to catch this for general-purpose error handling.

ArithmeticError: Base class for arithmetic-related errors like OverflowError, ZeroDivisionError, and FloatingPointError.

BufferError: Raised when a buffer-related operation cannot be performed.

LookupError: Base class for errors that occur when a lookup on a collection fails, such as IndexError (for sequences) and KeyError (for mappings).

FileNotFoundError: Raised when an attempt to open a file fails because the file does not exist.

IOError (or OSError in newer versions of Python): Raised for errors related to input/output operations, like failing to read or write to a file.

ValueError: Raised when a function receives an argument of the correct type but an inappropriate value.

TypeError: Raised when an operation or function is applied to an object of an inappropriate type.

ZeroDivisionError: Raised when the second argument of a division or modulo operation is zero.

KeyError: Raised when a dictionary key or set element is not found.

IndexError: Raised when a sequence subscript (index) is out of range.'''